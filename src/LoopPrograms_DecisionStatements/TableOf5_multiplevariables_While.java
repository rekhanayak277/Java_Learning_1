package LoopPrograms_DecisionStatements;

public class TableOf5_multiplevariables_While {

    public static void main(String[] args) {
        int num1 = 5 ;

        int num2 = 1;

        while (num2 <= 10)
        {

            int result = num1 * num2;

            System.out.println(num1 + "*" + num2 + "=" + result);

            num2++;
        }
    }
}




