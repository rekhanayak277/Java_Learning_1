package TypeCasting;

public class DemoImplicitTypeCasting {


    public static void main(String[] args) {

        int Value1 = 10; //   initializing int type

        long Value2 =Value1; // changing to Long type

        float Value3=Value2; // changing to float type

        double Value4 = Value3; // Changing to double type

        System.out.println("int value is :"+ Value1);
        System.out.println("long value is : " + Value2);
        System.out.println("float value is : " + Value3);
        System.out.println("double value is : " + Value4);

    }
}

