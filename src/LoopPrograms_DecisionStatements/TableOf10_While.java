package LoopPrograms_DecisionStatements;

public class TableOf10_While {
     /*
    Table of 10
        Logic here:
    Print - 10 20 30 40 .......200
    Use a variable, increment by 10
            */
     public static void main(String[] args) {


    int num = 10;

    while (num<=200) {

        System.out.println(num);

        num+=10;
    }
    }
}


