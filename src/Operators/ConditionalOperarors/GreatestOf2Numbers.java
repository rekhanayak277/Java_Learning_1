package Operators.ConditionalOperarors;

// Write a program to find the greatest of 2 numbers using conditional operator

public class GreatestOf2Numbers {

    public static void main(String[] args) {

        double num1 = 12.5;

        double num2 = 15.2;

        // ( condition ) ? exp1 : exp2

       double greatest =  ( num1 >= num2 ) ?  num1 : num2 ;

        System.out.println(" The greatest of 2 numbers is " + greatest);

    }





}
